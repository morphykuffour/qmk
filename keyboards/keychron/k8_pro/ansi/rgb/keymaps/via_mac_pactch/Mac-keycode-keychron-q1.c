bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case KC_MISSION_CONTROL:
      HCS(0x29F);
    case KC_SPOTLIGHT:
      HCS(0x221);
    case KC_DICTATION:
      HCS(0xCF);
    case KC_DO_NOT_DISTURB:
      HSS(0x9B);
    case KC_LOCK_SCREEN:
      HCS(0x19E);
    default:
      return true;
  }
}
